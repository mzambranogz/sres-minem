    SELECT
    (SELECT COUNT(*) FROM T_GEND_CONV_CRI_CASO C WHERE C.ID_CONVOCATORIA = PI_ID_CONVOCATORIA AND C.ID_CRITERIO = PI_ID_CRITERIO AND C.ID_CASO = PI_ID_CASO)
