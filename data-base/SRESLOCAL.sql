CREATE TABLE T_MAE_SECTOR(
ID_SECTOR NUMBER,
NOMBRE VARCHAR2(50),
FLAG_ESTADO VARCHAR2(1) DEFAULT '1',
REG_USUARIO NUMBER,
REG_FECHA DATE DEFAULT SYSDATE,
UDP_USUARIO NUMBER,
UPD_FECHA DATE,
CONSTRAINT SECTOR_PK PRIMARY KEY(ID_SECTOR)
);

CREATE TABLE  T_GENM_INSTITUCION(
ID_INSTITUCION NUMBER,
RUC NUMBER(11,0),
RAZON_SOCIAL VARCHAR2(100),
DOMICILIO_LEGAL VARCHAR2(120),
ID_SECTOR NUMBER,
FLAG_ESTADO VARCHAR2(1) DEFAULT '1',
REG_USUARIO NUMBER,
REG_FECHA DATE DEFAULT SYSDATE,
UDP_USUARIO NUMBER,
UPD_FECHA DATE,
CONSTRAINT INSTITUCION_PK PRIMARY KEY(ID_INSTITUCION),
CONSTRAINT SECTOR_FK FOREIGN KEY (ID_SECTOR) REFERENCES T_MAE_SECTOR(ID_SECTOR)
);

CREATE TABLE T_MAE_ROL(
ID_ROL NUMBER,
NOMBRE VARCHAR(50),
FLAG_ESTADO VARCHAR2(1) DEFAULT '1',
REG_USUARIO NUMBER,
REG_FECHA DATE DEFAULT SYSDATE,
UDP_USUARIO NUMBER,
UPD_FECHA DATE,
CONSTRAINT ROL_PK PRIMARY KEY (ID_ROL)
);

CREATE TABLE T_GENM_USUARIO (
ID_USUARIO NUMBER,
NOMBRES VARCHAR2(30),
APELLIDOS VARCHAR2(50),
CORREO VARCHAR2(100),
CONTRASENA VARCHAR2(100),
TELEFONO VARCHAR2(10),
ANEXO VARCHAR2(6),
CELULAR VARCHAR2(12),
ID_INSTITUCION NUMBER,
ID_ROL NUMBER,
FLAG_ESTADO VARCHAR2(1) DEFAULT '1',
REG_USUARIO NUMBER,
REG_FECHA DATE DEFAULT SYSDATE,
UPD_USUARIO NUMBER,
UPD_FECHA DATE,
CONSTRAINT USUARIO_PK PRIMARY KEY (ID_USUARIO),
CONSTRAINT INSTITUCION_FK FOREIGN KEY (ID_INSTITUCION) REFERENCES T_GENM_INSTITUCION (ID_INSTITUCION),
CONSTRAINT ROL_FK FOREIGN KEY (ID_ROL) REFERENCES T_MAE_ROL (ID_ROL)
);

CREATE TABLE T_GENM_CRITERIO(
ID_CRITERIO NUMBER,
NOMBRE VARCHAR2(100),
FLAG_ESTADO VARCHAR2(1) DEFAULT '1',
REG_USUARIO NUMBER,
REG_FECHA DATE DEFAULT SYSDATE,
UDP_USUARIO NUMBER,
UPD_FECHA DATE,
CONSTRAINT CRITERIO_PK PRIMARY KEY (ID_CRITERIO)
);

CREATE TABLE T_GENM_REQUERIMIENTO(
ID_REQUERIMIENTO NUMBER,
NOMBRE VARCHAR2(300),
FLAG_ESTADO VARCHAR2(1) DEFAULT '1',
REG_USUARIO NUMBER,
REG_FECHA DATE DEFAULT SYSDATE,
UDP_USUARIO NUMBER,
UPD_FECHA DATE,
CONSTRAINT REQUERIMIENTO_PK PRIMARY KEY (ID_REQUERIMIENTO)
);

CREATE TABLE T_MAE_PROCESO(
ID_PROCESO NUMBER,
NOMBRE VARCHAR2(200),
FLAG_ESTADO VARCHAR2(1) DEFAULT '1',
REG_USUARIO NUMBER,
REG_FECHA DATE DEFAULT SYSDATE,
UDP_USUARIO NUMBER,
UPD_FECHA DATE,
CONSTRAINT PROCESO_PK PRIMARY KEY (ID_PROCESO)
);

CREATE TABLE T_MAE_ETAPA(
ID_ETAPA NUMBER,
NOMBRE VARCHAR2(200),
ID_PROCESO NUMBER,
FLAG_ESTADO VARCHAR2(1) DEFAULT '1',
REG_USUARIO NUMBER,
REG_FECHA DATE DEFAULT SYSDATE,
UDP_USUARIO NUMBER,
UPD_FECHA DATE,
CONSTRAINT ETAPA_PK PRIMARY KEY (ID_ETAPA),
CONSTRAINT PROCESO_FK FOREIGN KEY (ID_PROCESO) REFERENCES T_MAE_PROCESO (ID_PROCESO)
);


