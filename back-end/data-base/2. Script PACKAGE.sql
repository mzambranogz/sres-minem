  PROCEDURE USP_PRC_MEDIDA_RESULTADO(
    PI_ID_CONVOCATORIA NUMBER,
    PI_ID_INSCRIPCION NUMBER,
    PI_USUARIO_GUARDAR NUMBER
  ) AS
    vID NUMBER;
    vRECON NUMBER;
  BEGIN     
    SELECT NVL(ID_RECONOCIMIENTO,0) INTO vRECON FROM T_GENM_RECONOCIMIENTO WHERE ID_INSCRIPCION = PI_ID_INSCRIPCION;
    FOR CRSS IN(
                  SELECT
                  RM.ID_MEDMIT 
                  FROM T_GENM_RECONOCIMIENTO R
                  INNER JOIN T_GENM_INSCRIPCION INSC ON R.ID_INSCRIPCION = INSC.ID_INSCRIPCION
                  INNER JOIN T_GEND_RECONOCIMIENTO_MEDIDA RM ON R.ID_RECONOCIMIENTO = RM.ID_RECONOCIMIENTO
                  WHERE INSC.ID_CONVOCATORIA = PI_ID_CONVOCATORIA
                  GROUP BY RM.ID_MEDMIT
              )
              LOOP
                  SELECT ID_RECONOCIMIENTO INTO vID FROM
                  (
                  SELECT
                  RM.ID_RECONOCIMIENTO, SUM(RM.REDUCIDO) REDUCIDO  
                  FROM T_GENM_RECONOCIMIENTO R
                  INNER JOIN T_GENM_INSCRIPCION INSC ON R.ID_INSCRIPCION = INSC.ID_INSCRIPCION
                  INNER JOIN T_GEND_RECONOCIMIENTO_MEDIDA RM ON R.ID_RECONOCIMIENTO = RM.ID_RECONOCIMIENTO
                  WHERE INSC.ID_CONVOCATORIA = PI_ID_CONVOCATORIA AND RM.ID_MEDMIT = CRSS.ID_MEDMIT
                  GROUP BY RM.ID_RECONOCIMIENTO
                  ORDER BY REDUCIDO DESC
                  )
                  WHERE ROWNUM = 1;
                  
                  IF vID = vRECON THEN
                    UPDATE  T_GEND_RECON_MEDIDA_RESULTADO SET
                            OBTENIDO = '1',
                            UPD_USUARIO = PI_USUARIO_GUARDAR,
                            UPD_FECHA = SYSDATE
                    WHERE   ID_RECONOCIMIENTO = vID AND ID_MEDMIT = CRSS.ID_MEDMIT;
                  END IF;                  
              END LOOP;
              
              SELECT ID_RECONOCIMIENTO INTO vID FROM
              (
              SELECT --VALIDAR QUIEN TIENE MAS EMISIONES
              R.EMISIONES, R.ID_RECONOCIMIENTO 
              FROM T_GENM_RECONOCIMIENTO R
              INNER JOIN T_GENM_INSCRIPCION INSC ON R.ID_INSCRIPCION = INSC.ID_INSCRIPCION
              WHERE INSC.ID_CONVOCATORIA = PI_ID_CONVOCATORIA
              ORDER BY R.EMISIONES DESC
              )
              WHERE ROWNUM = 1;
              
              IF vRECON = vID THEN
                UPDATE  T_GENM_RECONOCIMIENTO SET
                        FLAG_EMISIONESMAX = '1',
                        UPD_USUARIO = PI_USUARIO_GUARDAR,
                        UPD_FECHA = SYSDATE
                WHERE   ID_RECONOCIMIENTO = vRECON;
              END IF;
              
  END USP_PRC_MEDIDA_RESULTADO;

